# OPERADORES COMPOSTOS

# Valor Sem alteração
x = 10
print(f'Vaariável nomal: x= {x}')

# Somando os valores na mesma variável.
x += 10
print(f'Soma: x= {x}')

# Subitraindo os valores da mesma variável.
x -= 5
print(f'Subitração: x= {x}')

# Multiplicando os valores da mesma variável.
x *= 2
print(f'Multiplicação: x= {x}')

# Dividindo os valores da mesma variável.
x /= 2
print(f'Divisão: x= {x}')

# Modulo (resto) do valor da mesma variável
x %= 2
print(f'Módulo: x= {x}')




